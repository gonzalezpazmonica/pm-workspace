async def _handle_summarize(
    args: dict[str, Any],
    base_dir: Path,
) -> list[TextContent]:
    """Handle the summarize() MCP tool call."""
    import json

    raw_turns: list[dict[str, Any]] = args.get("turns", [])
    run_id: str = str(args.get("run_id", ""))
    threshold_pct: int = int(args.get("threshold_pct", 75))
    recent_turns_count: int = int(args.get("recent_turns", 5))
    tier: str = str(args.get("tier", "fast"))
    confidentiality: str = str(args.get("confidentiality", "N1"))
    caller_confidentiality: str = str(args.get("caller_confidentiality", "N1"))
    force: bool = bool(args.get("force", False))

    if not run_id:
        return [TextContent(type="text", text='{"error": "run_id is required"}')]

    # Confidentiality check: caller must be >= resource
    try:
        _check_access(caller_confidentiality, confidentiality)
    except PermissionError as exc:
        return [
            TextContent(
                type="text",
                text=json.dumps({"error": "403 Forbidden", "detail": str(exc)}),
            )
        ]

    try:
        turns = _parse_turns(raw_turns)
    except ValueError as exc:
        return [
            TextContent(
                type="text",
                text=json.dumps({"error": f"Invalid turns input: {exc}"}),
            )
        ]

    # Monitor
    effective_threshold = 1 if force else threshold_pct
    config = ContextGuardConfig(
        enabled=True,
        threshold_pct=max(50, effective_threshold),
        recent_turns=recent_turns_count,
    )
    tokenizer = TierTokenizer(tier=tier)  # type: ignore[arg-type]
    monitor = ContextMonitor(config=config, tokenizer=tokenizer, tier=tier)  # type: ignore[arg-type]

    # When force=True we bypass the monitor threshold check
    if force:
        turns_to_summarize = turns[: max(0, len(turns) - recent_turns_count)]
        if not turns_to_summarize:
            turns_to_summarize = turns
    else:
        decision = monitor.check(turns)
        if not decision.should_summarize:
            return [
                TextContent(
                    type="text",
                    text=json.dumps(
                        {
                            "triggered": False,
                            "pct_used": round(decision.pct_used, 2),
                            "threshold_pct": threshold_pct,
                            "message": "Threshold not crossed. No summarization performed.",
                        }
                    ),
                )
            ]
        turns_to_summarize = decision.turns_to_summarize

    turns_text = "\n".join(
        f"[{t.role}] {t.content}" for t in turns_to_summarize
    )
    tokens_before = sum(tokenizer.count(t.content) for t in turns_to_summarize)

    # Summarize
    invoke_fn = _build_mock_invoke_fn(tier)
    summarizer = Summarizer(invoke_fn=invoke_fn, initial_tier=tier)  # type: ignore[arg-type]

    try:
        result = summarizer.summarize(turns_text=turns_text, tokens_before=tokens_before)
    except SummarizationError as exc:
        return [
            TextContent(
                type="text",
                text=json.dumps({"error": f"Summarization failed: {exc}"}),
            )
        ]

    # Persist
    store = SummaryStore(base_dir=base_dir, confidentiality=confidentiality)  # type: ignore[arg-type]
    summary_path = store.save(run_id=run_id, result=result)
    store.write_trace_event(run_id=run_id, result=result, summary_path=summary_path)

    return [
        TextContent(
            type="text",
            text=json.dumps(
                {
                    "triggered": True,
                    "summary_id": summary_path.stem,
                    "run_id": run_id,
                    "tokens_before": result.tokens_before,
                    "tokens_after": result.tokens_after,
                    "tier_used": result.tier_used,
                    "retried": result.retried,
                    "confidentiality": confidentiality,
                    "summary": result.summary.model_dump(),
                },
                ensure_ascii=False,
            ),
        )
    ]


async def _handle_recall_summary(
    args: dict[str, Any],
    base_dir: Path,
) -> list[TextContent]:
    """Handle the recall_summary() MCP tool call."""
    import json

    run_id: str = str(args.get("run_id", ""))
    summary_id: str | None = args.get("summary_id") or None
    caller_confidentiality: str = str(args.get("caller_confidentiality", "N1"))

    if not run_id:
        return [TextContent(type="text", text='{"error": "run_id is required"}')]

    # Try all confidentiality levels to find where this run lives.
    # We enforce the access check based on what we find.
    _levels = ["N4b", "N4", "N3", "N2", "N1"]
    data: dict | None = None
    found_level: str = "N1"

    for lvl in _levels:
        store = SummaryStore(base_dir=base_dir, confidentiality=lvl)  # type: ignore[arg-type]
        try:
            data = store.load(run_id=run_id, summary_id=summary_id)
            found_level = lvl
            break
        except FileNotFoundError:
            continue

    if data is None:
        return [
            TextContent(
                type="text",
                text=json.dumps({"error": f"No summaries found for run_id={run_id!r}"}),
            )
        ]

    # Enforce access: caller must be >= found resource level
    resource_confidentiality = data.get("_meta", {}).get("confidentiality", found_level)
    try:
        _check_access(caller_confidentiality, resource_confidentiality)
    except PermissionError as exc:
        return [
            TextContent(
                type="text",
                text=json.dumps({"error": "403 Forbidden", "detail": str(exc)}),
            )
        ]

    return [
        TextContent(
            type="text",
            text=json.dumps(data, ensure_ascii=False, default=str),
        )
    ]


# ---------------------------------------------------------------------------
# Entry point
# ---------------------------------------------------------------------------


async def run_server(base_dir: Path | None = None) -> None:
    """Run the MCP server over stdio."""
    server = build_server(base_dir=base_dir)
    async with stdio_server() as (read_stream, write_stream):
        await server.run(
            read_stream,
            write_stream,
            server.create_initialization_options(),
        )


def main() -> None:
    import asyncio

    logging.basicConfig(level=logging.WARNING, stream=sys.stderr)
    asyncio.run(run_server())


if __name__ == "__main__":
    main()
