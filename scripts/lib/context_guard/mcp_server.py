def build_server(base_dir: Path | None = None) -> Server:
    """Build and return the configured MCP server.

    Args:
        base_dir: Root directory for context-guard output.
            Defaults to ``<repo_root>/output/context-guard``.
    """
    if base_dir is None:
        # Resolve to <workspace>/output/context-guard at import time
        _here = Path(__file__).resolve()
        _workspace = _here.parents[3]  # scripts/lib/context_guard → workspace
        base_dir = _workspace / "output" / "context-guard"

    server: Server = Server(SERVER_NAME)

    @server.list_tools()  # type: ignore[misc]
    async def list_tools() -> list[Tool]:
        return [
            Tool(
                name="summarize",
                description=(
                    "Summarize a list of conversation turns and persist the summary. "
                    "Returns the summary_id and summary contents."
                ),
                inputSchema={
                    "type": "object",
                    "properties": {
                        "turns": {
                            "type": "array",
                            "items": {
                                "type": "object",
                                "properties": {
                                    "role": {"type": "string"},
                                    "content": {"type": "string"},
                                    "timestamp": {"type": "string"},
                                    "is_artifact": {"type": "boolean"},
                                },
                                "required": ["role", "content"],
                            },
                            "description": "Conversation turns to summarize.",
                        },
                        "run_id": {
                            "type": "string",
                            "description": "Unique identifier for this run/session.",
                        },
                        "threshold_pct": {
                            "type": "integer",
                            "minimum": 50,
                            "maximum": 95,
                            "default": 75,
                            "description": "Trigger threshold as % of context window.",
                        },
                        "recent_turns": {
                            "type": "integer",
                            "minimum": 1,
                            "default": 5,
                            "description": "Number of recent turns to preserve unsummarized.",
                        },
                        "tier": {
                            "type": "string",
                            "enum": ["heavy", "mid", "fast"],
                            "default": "fast",
                            "description": "Summarizer model tier.",
                        },
                        "confidentiality": {
                            "type": "string",
                            "enum": ["N1", "N2", "N3", "N4", "N4b"],
                            "default": "N1",
                            "description": "Confidentiality level inherited by the summary.",
                        },
                        "caller_confidentiality": {
                            "type": "string",
                            "enum": ["N1", "N2", "N3", "N4", "N4b"],
                            "default": "N1",
                            "description": "Confidentiality level of the calling context.",
                        },
                        "force": {
                            "type": "boolean",
                            "default": False,
                            "description": (
                                "Force summarization even if threshold not crossed. "
                                "Uses threshold_pct=1 internally to guarantee trigger."
                            ),
                        },
                    },
                    "required": ["turns", "run_id"],
                },
            ),
            Tool(
                name="recall_summary",
                description=(
                    "Retrieve a previously stored summary. "
                    "Returns the latest summary if summary_id is omitted."
                ),
                inputSchema={
                    "type": "object",
                    "properties": {
                        "run_id": {
                            "type": "string",
                            "description": "Run identifier used when the summary was created.",
                        },
                        "summary_id": {
                            "type": "string",
                            "description": (
                                "Specific summary id (e.g. 'summary-001'). "
                                "Omit to get the latest."
                            ),
                        },
                        "caller_confidentiality": {
                            "type": "string",
                            "enum": ["N1", "N2", "N3", "N4", "N4b"],
                            "default": "N1",
                            "description": "Confidentiality level of the calling context.",
                        },
                    },
                    "required": ["run_id"],
                },
            ),
        ]

    @server.call_tool()  # type: ignore[misc]
    async def call_tool(
        name: str,
        arguments: dict[str, Any],
    ) -> list[TextContent]:
        if name == "summarize":
            return await _handle_summarize(arguments, base_dir)
        if name == "recall_summary":
            return await _handle_recall_summary(arguments, base_dir)
        raise ValueError(f"Unknown tool: {name!r}")

    return server


