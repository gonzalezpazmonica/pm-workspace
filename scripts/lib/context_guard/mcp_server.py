"""Context Guard — MCP Server.

Exposes two MCP tools:
  - summarize(turns, run_id, threshold_pct?, recent_turns?, tier?, confidentiality?)
  - recall_summary(run_id, summary_id?)

Spec §2.5, §2.7 (D-7 Expone MCP server savia-context-guard).
Spec §2.8: confidencialidad — summary N3/N4 NOT accessible from N1 context.
Rule #26: Python for structured logic.
"""

from __future__ import annotations

import logging
import sys
from pathlib import Path
from typing import Any

from mcp.server import Server
from mcp.server.stdio import stdio_server
from mcp.types import TextContent, Tool

from scripts.lib.context_guard.monitor import ContextGuardConfig, ContextMonitor, TurnMessage
from scripts.lib.context_guard.store import SummaryStore
from scripts.lib.context_guard.summarizer import Summarizer, SummarizationError, build_summarizer_prompt
from scripts.lib.context_guard.tokenizer import TierTokenizer

logger = logging.getLogger(__name__)

# ---------------------------------------------------------------------------
# Confidentiality level ordering (higher index = more restricted)
# ---------------------------------------------------------------------------
_CONFIDENTIALITY_ORDER: list[str] = ["N1", "N2", "N3", "N4", "N4b"]

SERVER_NAME = "savia-context-guard"
SERVER_VERSION = "0.2.0"


def _confidentiality_index(level: str) -> int:
    try:
        return _CONFIDENTIALITY_ORDER.index(level)
    except ValueError:
        return 0


def _check_access(
    caller_level: str,
    resource_level: str,
) -> None:
    """Raise PermissionError if caller cannot access resource_level.

    Spec §2.8: un summary N3 no es accesible desde un contexto N1.
    Rule: caller must have equal or higher confidentiality level.
    """
    if _confidentiality_index(caller_level) < _confidentiality_index(resource_level):
        raise PermissionError(
            f"Access denied: caller confidentiality={caller_level!r} cannot access "
            f"summary with confidentiality={resource_level!r}. "
            "Spec §2.8 — summaries never exposed to lower confidentiality contexts."
        )


def _build_mock_invoke_fn(tier: str) -> Any:
    """Build a summarizer invoke_fn stub for the MCP context.

    In production this would call the actual OpenCode/Anthropic API.
    The MCP server operates in-process; the real invocation path is via
    the context-summarizer agent (separate process). This stub produces
    a valid summary_v1 YAML so the MCP server can be tested end-to-end
    without a live LLM call.
    """
    import yaml  # local import — only needed here

    def _invoke(turns_text: str, invoke_tier: str) -> str:
        word_count = len(turns_text.split())
        payload: dict[str, Any] = {
            "summary_v1": {
                "turn_count": turns_text.count("\n[") + 1,
                "time_span": {
                    "first_turn_at": "",
                    "last_turn_at": "",
                },
                "key_decisions": [],
                "artifacts_produced": [],
                "errors_encountered": [],
                "tools_invoked": [],
                "prose_summary": (
                    f"Summary generated by savia-context-guard MCP server "
                    f"(tier={invoke_tier}, words={word_count})."
                ),
            }
        }
        return yaml.dump(payload, allow_unicode=True)

    return _invoke


def _parse_turns(raw_turns: list[dict[str, Any]]) -> list[TurnMessage]:
    """Convert raw dicts from MCP input into TurnMessage objects."""
    result: list[TurnMessage] = []
    for item in raw_turns:
        if not isinstance(item, dict):
            raise ValueError(f"Each turn must be a dict, got {type(item).__name__}.")
        result.append(
            TurnMessage(
                role=str(item.get("role", "user")),
                content=str(item.get("content", "")),
                timestamp=item.get("timestamp"),
                is_artifact=bool(item.get("is_artifact", False)),
            )
        )
    return result


